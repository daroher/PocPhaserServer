package vo;

public class BismarckData {

	float franceX;
	float franceY;

	public BismarckData() {
		super();
	}

	public float getFranceX() {
		return franceX;
	}

	public void setFranceX(float franceX) {
		this.franceX = franceX;
	}

	public float getFranceY() {
		return franceY;
	}

	public void setFranceY(float franceY) {
		this.franceY = franceY;
	}
}
